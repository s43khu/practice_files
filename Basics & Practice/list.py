# CREATING A LIST
l = [1, 56, 67, 32, 17, 26]
print(l)
# SORTING A LIST
l.sort()
print(l)
# REVERSING THE DATA
l.reverse()
print(l)
# INSERTING 44 AT THE END OF LIST
l.append(44)
print(l)
# INSERTING 50 AT THE 3rd(actual 2nd) POSITION
l.insert(3, 50)
print(l)
l.sort()
print(l)
# DELETING THE 5th(actual 4th) VALUE
l.pop(5)
print(l)
# DELETE THE 44 FROM THE LIST
l.remove(44)
print(l)
